 * @method User[]    findAll()
 * @method User[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class UserRepository extends ServiceEntityRepository implements UserLoaderInterface
{
    private $property;

    public function __construct(ManagerRegistry $registry, string $entityClass = User::class, string $property = "")
    {
        $this->property = $property;
        parent::__construct($registry, $entityClass);
    }

    public function loadUserByUsername(string $username): ?User
    {
        trigger_deprecation('symfony/doctrine-bridge', '5.3', 'Method "%s()" is deprecated, use loadUserByIdentifier() instead.', __METHOD__);

        return $this->loadUserByIdentifier($username);
    }

    public function loadUserByIdentifier(string $identifier): ?User
    {
        return $this->findOneBy([$this->property => $identifier]);
    }

